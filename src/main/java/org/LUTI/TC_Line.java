package org.LUTI;

import org.jgrapht.Graph;
import org.jgrapht.graph.DefaultEdge;

///////////////////////////////////////////////////////////////////////////////////////////////////
/**                                   Ligne de transport en commun                               */
///////////////////////////////////////////////////////////////////////////////////////////////////
public class TC_Line
{
    String id;
    String type;
    private final Graph<Node, DefaultEdge> graph;
    ///////////////////////////////////////////////////////////////////////////////////////////////////
    /**                                        Constructeur                                          */
    ///////////////////////////////////////////////////////////////////////////////////////////////////
    public TC_Line(String id, String type, Graph<Node, DefaultEdge> graph)
    {
        this.id = id;
        this.type = type;
        this.graph = graph;
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////////
    /**                                    Accesseurs                                                */
    ///////////////////////////////////////////////////////////////////////////////////////////////////
    public String getId(){return this.id;}
    public String getType(){return this.type;}
    public Graph<Node, DefaultEdge> getGraph(){return this.graph;};
}
